package dev.dazzle.experiment.objectbox

import io.objectbox.annotation.Entity
import io.objectbox.annotation.HnswIndex
import io.objectbox.annotation.Id
import io.objectbox.annotation.Index
import io.objectbox.annotation.VectorDistanceType

/**
 * One vector row for the dazzle-vs-peers benchmark. ObjectBox 4.x/5.x supports
 * on-device HNSW directly via [HnswIndex] on a `FloatArray` property.
 *
 * [externalId] stores the benchmark's string id (e.g. "doc:42") so the recall
 * evaluator can line up results with the SQLite brute-force ground truth.
 *
 * The vector dim is configured at compile time on the annotation, so the
 * benchmark harness always uses the largest dim we care about (384) and pads
 * shorter vectors with zeros. Padding with zeros is a no-op for cosine after
 * normalisation — it just leaves the index overprovisioned for small configs.
 */
@Entity
data class VectorEntity(
    @Id var id: Long = 0,
    @Index var externalId: String = "",
    // Match dazzle-vector's HNSW build params so the comparison is between
    // HNSW implementations, not between a tuned build and a default one:
    //   neighborsPerNode   = M             (dazzle/hnswlib use M=32)
    //   indexingSearchCount = efConstruction (dazzle/hnswlib use efC=400)
    // ObjectBox defaults (M=30, efC=100) otherwise give it lower recall at
    // build time than it's actually capable of.
    @HnswIndex(
        dimensions = 384,
        neighborsPerNode = 32L,
        indexingSearchCount = 400L,
        distanceType = VectorDistanceType.COSINE,
    )
    var embedding: FloatArray = FloatArray(0),
) {
    // equals/hashCode generated by data class collide with FloatArray identity
    // semantics — ObjectBox doesn't need them, so the defaults are fine but
    // suppress lint with explicit overrides.
    override fun equals(other: Any?): Boolean = this === other
    override fun hashCode(): Int = id.hashCode()
}
