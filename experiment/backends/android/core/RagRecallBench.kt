package dev.dazzle.experiment

import android.content.Context
import android.os.Build
import android.os.Environment
import android.util.Log
import com.google.gson.GsonBuilder
import dev.dazzle.sdk.DazzleConfig
import dev.dazzle.sdk.DazzleModule
import dev.dazzle.sdk.DazzlePersistence
import dev.dazzle.sdk.DazzleServer
import dev.dazzle.sdk.VectorIndex
import dev.dazzle.sdk.WipeTarget
import java.io.BufferedReader
import java.io.File
import java.io.InputStreamReader
import org.json.JSONArray
import org.json.JSONObject

/**
 * E2 RAG retrieval recall bench — BGE-small-q4 embedder + dazzle-vector HNSW.
 *
 * Pipeline on every query:
 *   1. embed query text (llama.cpp GGUF → FloatArray[384])
 *   2. searchDirect top-k against dazzle-vector HNSW (dim=384 f32)
 *   3. recall@{1,5,10} against the gold passage id from the NQ slice
 *
 * Slice lives in assets/nq_slice/ and is generated by
 * research/scripts/nq_slice.py from sentence-transformers/natural-questions
 * — 2000 passages, 200 queries, one gold passage per query.
 *
 * Invocation:
 *   adb shell am start -n dev.dazzle.experiment.backends/.BackendsActivity \
 *     --ez test_storage_only true --es backend rag-bench
 *
 * Output: /sdcard/Documents/rag_<model>_<ts>.json — one per run.
 */
object RagRecallBench {

    private const val TAG = "RagBench"
    private val gson = GsonBuilder().setPrettyPrinting().create()

    data class Config(
        val modelFile:      String = "bge-small-en-v1.5-q4_k_m.gguf",
        val modelSource:    String? = null,       // null → <filesDir>/embed/
        val nCtx:           Int = 512,
        val indexName:      String = "nq:vindex",
        val hashPrefix:     String = "nq:",
        val k:              Int = 10,             // top-k we retrieve
        val efRuntime:      Int = 64,             // HNSW search-time quality
        val passagesAsset:  String = "nq_slice/passages.jsonl",
        val queriesAsset:   String = "nq_slice/queries.jsonl",
    )

    private data class Passage(val id: String, val text: String)
    private data class Query   (val id: String, val text: String, val gold: List<String>)

    fun run(context: Context, cfg: Config = Config()) {
        Log.i(TAG, "══ RagRecallBench model=${cfg.modelFile} ══")

        // ── 1. Boot a vector-search server (its own port — 6382 to not ───
        // collide with the Plan-18 server on 6380).
        if (DazzleServer.isRunning()) DazzleServer.stop()
        DazzleServer.start(context, DazzleConfig(
            port        = 6382,
            persistence = DazzlePersistence.None,
            wipeOnStart = setOf(WipeTarget.AOF, WipeTarget.RDB),
            modules     = setOf(DazzleModule.VectorSearch),
        ))
        Thread.sleep(600)

        try {
            val out = runInner(context, cfg)
            val safeModel = Build.MODEL.replace(Regex("[^A-Za-z0-9_-]"), "_")
            val ts = System.currentTimeMillis()
            val fname = "rag_${safeModel}_${ts}.json"
            val file = try {
                val docs = Environment.getExternalStoragePublicDirectory(
                    Environment.DIRECTORY_DOCUMENTS,
                )
                docs.mkdirs()
                File(docs, fname)
            } catch (_: Exception) {
                File(context.filesDir, fname)
            }
            file.writeText(gson.toJson(out))
            Log.i(TAG, "══ wrote ${file.absolutePath} ══")
        } finally {
            try { DazzleServer.stop() } catch (_: Throwable) {}
        }
    }

    private fun runInner(context: Context, cfg: Config): Map<String, Any?> {
        // ── 2. Open the embedder ─────────────────────────────────────────
        val sourceDir = cfg.modelSource?.let { File(it) }
            ?: File(context.filesDir, "embed").apply { mkdirs() }
        val source = File(sourceDir, cfg.modelFile)
        check(source.exists()) {
            "GGUF not found at ${source.absolutePath}. Push with: " +
                "adb shell run-as <pkg> cp /data/local/tmp/${cfg.modelFile} files/embed/"
        }
        val embedder = DazzleEmbedder.open(context, source.absolutePath, nCtx = cfg.nCtx)
        Log.i(TAG, "embedder open: n_embd=${embedder.outputDim}")

        // ── 3. Load the NQ slice from assets ─────────────────────────────
        val passages = loadPassages(context, cfg.passagesAsset)
        val queries  = loadQueries (context, cfg.queriesAsset)
        Log.i(TAG, "slice: ${passages.size} passages, ${queries.size} queries")

        val result = linkedMapOf<String, Any?>(
            "type"      to "rag_recall",
            "timestamp" to java.time.Instant.now().toString(),
            "device"    to collectDeviceInfo(),
            "model"     to linkedMapOf(
                "file"       to cfg.modelFile,
                "output_dim" to embedder.outputDim,
                "n_ctx"      to cfg.nCtx,
                "size_bytes" to source.length(),
            ),
            "slice" to linkedMapOf(
                "n_passages" to passages.size,
                "n_queries"  to queries.size,
            ),
        )

        try {
            // ── 4. Build the HNSW index — pre-sized, since we know N ─────
            val dazzle = DazzleServer.client()
            val index = dazzle.vectorIndex(
                name            = cfg.indexName,
                hashPrefix      = cfg.hashPrefix,
                vectorField     = "emb",
                dim             = embedder.outputDim,
                algorithm       = VectorIndex.Algorithm.HNSW,
                metric          = VectorIndex.Metric.COSINE,
                initialCapacity = passages.size,
                m               = 16,
                efConstruction  = 200,
            )
            index.create()

            // ── 5. Embed + ingest all passages ────────────────────────────
            val embedIngestStart = System.nanoTime()
            val passageEmbedLats = LongArray(passages.size)
            val ids  = Array(passages.size) { "${cfg.hashPrefix}${passages[it].id}" }
            val vecs = Array(passages.size) { i ->
                val t0 = System.nanoTime()
                val v = embedder.embed(passages[i].text)
                passageEmbedLats[i] = (System.nanoTime() - t0) / 1_000L
                if (i % 200 == 0) Log.i(TAG, "  embed passage $i/${passages.size}")
                v
            }
            val embedTotalMs = (System.nanoTime() - embedIngestStart) / 1_000_000L
            Log.i(TAG, "embedded ${passages.size} passages in ${embedTotalMs} ms")

            val addStart = System.nanoTime()
            index.addBatchDirect(ids, vecs)
            val addMs = (System.nanoTime() - addStart) / 1_000_000L
            Log.i(TAG, "indexed ${passages.size} vectors in ${addMs} ms")

            // ── 6. Warm-up: run a handful of queries untimed. Mirrors the
            // pattern in StorageOnlyTest — JIT + HNSW cache priming.
            val warmupN = minOf(5, queries.size)
            for (i in 0 until warmupN) {
                val v = embedder.embed(queries[i].text)
                index.searchDirect(v, k = cfg.k, efRuntime = cfg.efRuntime)
            }

            // ── 7. Timed query loop ──────────────────────────────────────
            var hits1   = 0
            var hits5   = 0
            var hits10  = 0
            val qEmbed  = LongArray(queries.size)
            val qSearch = LongArray(queries.size)
            for ((qi, q) in queries.withIndex()) {
                val goldKeys = q.gold.map { cfg.hashPrefix + it }.toSet()
                val t0 = System.nanoTime()
                val qv = embedder.embed(q.text)
                val t1 = System.nanoTime()
                val hits = index.searchDirect(qv, k = cfg.k, efRuntime = cfg.efRuntime)
                val t2 = System.nanoTime()
                qEmbed [qi] = (t1 - t0) / 1_000L
                qSearch[qi] = (t2 - t1) / 1_000L

                val topIds = hits.map { it.first }
                if (topIds.firstOrNull() in goldKeys) hits1++
                if (topIds.take(5 ).any { it in goldKeys }) hits5++
                if (topIds.take(10).any { it in goldKeys }) hits10++
            }

            val recall1  = hits1  .toDouble() / queries.size
            val recall5  = hits5  .toDouble() / queries.size
            val recall10 = hits10 .toDouble() / queries.size
            Log.i(TAG, "recall@1=${"%.3f".format(recall1)}  " +
                       "recall@5=${"%.3f".format(recall5)}  " +
                       "recall@10=${"%.3f".format(recall10)}")

            result["index"] = linkedMapOf(
                "algorithm"        to "HNSW",
                "metric"           to "COSINE",
                "m"                to 16,
                "ef_construction"  to 200,
                "ef_runtime"       to cfg.efRuntime,
                "passage_embed_ms" to embedTotalMs,
                "passage_add_ms"   to addMs,
                "passage_embed_us" to latencyStats(passageEmbedLats),
            )
            result["query"] = linkedMapOf(
                "k"             to cfg.k,
                "embed_us"      to latencyStats(qEmbed),
                "search_us"     to latencyStats(qSearch),
            )
            result["recall"] = linkedMapOf(
                "at_1"  to recall1,
                "at_5"  to recall5,
                "at_10" to recall10,
            )
        } finally {
            embedder.close()
        }
        return result
    }

    // ── Slice I/O ────────────────────────────────────────────────────────

    private fun loadPassages(context: Context, asset: String): List<Passage> {
        val out = ArrayList<Passage>(4096)
        context.assets.open(asset).use { s ->
            BufferedReader(InputStreamReader(s, Charsets.UTF_8)).useLines { lines ->
                for (line in lines) {
                    if (line.isBlank()) continue
                    val j = JSONObject(line)
                    out += Passage(j.getString("_id"), j.getString("text"))
                }
            }
        }
        return out
    }

    private fun loadQueries(context: Context, asset: String): List<Query> {
        val out = ArrayList<Query>(256)
        context.assets.open(asset).use { s ->
            BufferedReader(InputStreamReader(s, Charsets.UTF_8)).useLines { lines ->
                for (line in lines) {
                    if (line.isBlank()) continue
                    val j = JSONObject(line)
                    val gArr = j.optJSONArray("gold") ?: JSONArray()
                    val gold = ArrayList<String>(gArr.length())
                    for (i in 0 until gArr.length()) gold += gArr.getString(i)
                    out += Query(j.getString("_id"), j.getString("text"), gold)
                }
            }
        }
        return out
    }

    // ── Stats + metadata ─────────────────────────────────────────────────

    private fun latencyStats(vs: LongArray): Map<String, Any?> {
        if (vs.isEmpty()) return emptyMap()
        val sorted = vs.copyOf().also { it.sort() }
        fun pct(p: Double): Long = sorted[minOf((sorted.size * p).toInt(), sorted.size - 1)]
        return linkedMapOf(
            "n"   to sorted.size,
            "avg" to sorted.sum().toDouble() / sorted.size,
            "p50" to pct(0.50),
            "p95" to pct(0.95),
            "p99" to pct(0.99),
            "min" to sorted.first(),
            "max" to sorted.last(),
        )
    }

    private fun collectDeviceInfo(): Map<String, Any?> = linkedMapOf(
        "model"           to Build.MODEL,
        "manufacturer"    to Build.MANUFACTURER,
        "board"           to Build.BOARD,
        "abi"             to Build.SUPPORTED_ABIS.firstOrNull(),
        "android_version" to Build.VERSION.RELEASE,
        "sdk_int"         to Build.VERSION.SDK_INT,
        "cpu_cores"       to Runtime.getRuntime().availableProcessors(),
    )
}
